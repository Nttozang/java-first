public class DataType {
    public static void main(String[] args){
        int myNum = 1000;
        long myNum1 = 15000000000L;
        byte myNum2 = 121;
        short myNum3 = 100;
        double myNum4 = 19.99d;

        System.out.println("int = "+myNum);
        System.out.println("long = "+myNum1);
        System.out.println("b = "+myNum2);
        System.out.println("s = "+myNum3);
        System.out.println("d = "+myNum4);

        float myNum5 = 5.75f;
        char myGrade = 'S';

        System.out.println("long = "+myNum5);
        System.out.println("long = "+myGrade);

        boolean isJavaFun = true;
        boolean isJavaBad = false;

        System.out.println("long = "+isJavaFun);
        System.out.println("long = "+isJavaBad);
    }
}
